export class BasicCalculate{
    public static add(x: number, y: number): number {
        return x + y;
    }
    public static subtract(x: number, y: number): number {
        return x - y;
    }
    public static multiple(x: number, y: number): number {
        return x*y;
    }
    public static division(x: number, y: number): number {
        return x/y;
    }
}